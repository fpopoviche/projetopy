#%%

#soma
print('1+1 =', 1 + 1)

#subtração
print('10-5 =', 10 - 5)

#%%

#multiplicação
print('10x5 = ', 10*5)

#divisão
print('10/3 = ', 10/3)

#divisão com inteira parte
# `//` serve para dividir e mostrar apenas a parte inteira do resultado, por exemplo `3` ao inves de `3.333`
print('10//3 = ', 10//3)

#resto da divisao
#mostra o valor que sobra da divisao ex `10/4 = 2.5`
# 10-4 da 6 e 6-4 da 2, essa sobra resulta o calculo atarves do mod %
print('10%4 = ', 10%4)

#%%

# potencia
print('3 ** 4 =', 3 ** 4)
